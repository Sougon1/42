#include<stdio.h>

int	ft_str_is_uppercase(char *str)
{
	int	j;

	j = 0;
	while (str[j] >= 65 && str[j] <= 90)
	{
		j++;
	}
	if (str[j] == '\0')
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
