#include<stdio.h>

int	ft_str_is_lowercase(char *str)
{
	int	j;

	j = 0;
	while (str[j] >= 97 && str[j] <= 122)
	{
		j++;
	}
	if (str[j] == '\0')
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
