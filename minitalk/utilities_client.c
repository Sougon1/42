#include "minitalk.h"

int	ft_atoi(const char *nptr)
{
	int	sign;
	int	valeur;

	sign = 1;
	valeur = 0;
	while ((*nptr >= 9 && *nptr <= 13) || *nptr == 32)
		nptr++;
	if (*nptr == 45 || *nptr == 43)
	{
		if (*nptr == 45)
			sign *= -1;
		nptr++;
	}
	while (*nptr >= 48 && *nptr <= 57)
	{
		valeur = (valeur * 10) + (*nptr - 48);
		nptr++;
	}
	return (valeur * sign);
}

size_t	ft_strlen(const char *str)
{
	size_t	length;

	length = 0;
	while (str[length] != '\0')
	{
		length++;
	}
	return (length);
}
